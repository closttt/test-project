export function latestVersion(): string {
  return CHANGELOG[0]?.version ?? "1.0";
}

const SEEN_KEY = "crm-changelog-seen-v1";

export function loadLastSeen(): string {
  return localStorage.getItem(SEEN_KEY) ?? "";
}

export function markSeen() {
  localStorage.setItem(SEEN_KEY, latestVersion());
}

export function hasUnseen(): boolean {
  return loadLastSeen() !== latestVersion();
}
