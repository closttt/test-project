function seed(): PlannedItem[] {
  return DEFAULT_PLANNED.map((d) => ({ ...d, id: uid(), done: false }));
}

/**
 * Titles ever offered to the user (seeded OR merged in later), so a fresh backlog item
 * added to DEFAULT_PLANNED in a later session gets merged into Story automatically —
 * but only ONCE. If the user deletes it afterward, it's marked "seen" and stays gone.
 */
const SEEN_KEY = "crm-roadmap-seen-v1";

function loadSeen(): Set<string> {
  try {
    return new Set(JSON.parse(localStorage.getItem(SEEN_KEY) ?? "[]") as string[]);
  } catch {
    return new Set();
  }
}

function saveSeen(seen: Set<string>) {
  localStorage.setItem(SEEN_KEY, JSON.stringify([...seen]));
}

export function loadPlanned(): PlannedItem[] {
  try {
    const raw = localStorage.getItem(KEY);
    if (!raw) {
      const fresh = seed();
      localStorage.setItem(KEY, JSON.stringify(fresh));
      saveSeen(new Set(DEFAULT_PLANNED.map((d) => d.title)));
      return fresh;
    }
    const items = JSON.parse(raw) as PlannedItem[];
    // Shipped or already-completed items are removed — the plan lists only what's left.
    let pruned = items.filter((it) => !it.done && !SHIPPED_TITLES.includes(it.title));

    // Merge in any backlog entries new since the user's last load (e.g. a fresh spec/roadmap
    // pass added them to DEFAULT_PLANNED) — every spec's output ends up in Story, not just docs.
    const seen = loadSeen();
    const existingTitles = new Set(pruned.map((it) => it.title));
    const newEntries = DEFAULT_PLANNED.filter(
      (d) => !seen.has(d.title) && !existingTitles.has(d.title) && !SHIPPED_TITLES.includes(d.title)
    );
    if (newEntries.length > 0) {
      pruned = [...pruned, ...newEntries.map((d) => ({ ...d, id: uid(), done: false }))];
    }
    DEFAULT_PLANNED.forEach((d) => seen.add(d.title));
    saveSeen(seen);

    if (pruned.length !== items.length || newEntries.length > 0) {
      localStorage.setItem(KEY, JSON.stringify(pruned));
    }
    return pruned;
  } catch {
    return seed();
  }
}

export function savePlanned(items: PlannedItem[]) {
  localStorage.setItem(KEY, JSON.stringify(items));
}

/** Reset to the default seed (a fresh set of ids). */
export function resetPlanned(): PlannedItem[] {
  const fresh = seed();
  savePlanned(fresh);
  saveSeen(new Set(DEFAULT_PLANNED.map((d) => d.title)));
  return fresh;
}
